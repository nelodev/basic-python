name = input("What's your name?: ")

#UPPER - Capital letters
print(name.upper())

#CAPITALIZE - First capital letter
print(name.capitalize())

#STRIP - Lowercase
print(name.lower())

#STRIP - Remove blanks
print(name.strip())

#REPLACE - Replace chars
print(name.replace('o', 'a'))

#CHAR - Get chars
print(name[0])

#LEN - Length of a string
print(len(name))
