nombre = input("¿Cuál es tu nombre?: ")
# 'facundo '

#UPPER - Mayúsculas
print(nombre.upper())

#CAPITALIZE - Primera letra mayúscula
print(nombre.capitalize())

#STRIP - Minúsculas
print(nombre.lower())

#STRIP - Eliminar espacios en blanco
print(nombre.strip())

#REPLACE - Sustituir chars
print(nombre.replace('o', 'a'))

#CHAR - Acceder a chars
print(nombre[0])

#LEN - Longitud de string
print(len(nombre))
